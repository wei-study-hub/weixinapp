public class TrainerModel {
	
	private String trainerCode;
	private String trainerName;
	private String trainerTitle;
	private String trainerImgUrl;
	
	public String getTrainerCode() {
		return trainerCode;
	}
	public void setTrainerCode(String trainerCode) {
		this.trainerCode = trainerCode;
	}
	public String getTrainerName() {
		return trainerName;
	}
	public void setTrainerName(String trainerName) {
		this.trainerName = trainerName;
	}
	public String getTrainerTitle() {
		return trainerTitle;
	}
	public void setTrainerTitle(String trainerTitle) {
		this.trainerTitle = trainerTitle;
	}
	public String getTrainerImgUrl() {
		return trainerImgUrl;
	}
	public void setTrainerImgUrl(String trainerImgUrl) {
		this.trainerImgUrl = trainerImgUrl;
	}
	
}
